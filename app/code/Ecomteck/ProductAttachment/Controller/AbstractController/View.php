namespace Ecomteck\ProductAttachment\Controller\AbstractController;

use Magento\Framework\App\Action;
use Magento\Framework\View\Result\PageFactory;

abstract class View extends Action\Action
{
    /**
     * @var \Ecomteck\ProductAttachment\Controller\AbstractController\ProductAttachmentLoaderInterface
     */
    private $productAttachmentLoader;
    
    /**
     * @var PageFactory
     */
    private $resultPageFactory;

    /**
     * @param Action\Context $context
     * @param ProductAttachmentLoaderInterface $productAttachmentLoader
     * @param PageFactory $resultPageFactory
     */
    public function __construct(
        Action\Context $context,
        ProductAttachmentLoaderInterface $productAttachmentLoader,
        PageFactory $resultPageFactory
    ) {
        $this->productAttachmentLoader = $productAttachmentLoader;
        $this->resultPageFactory = $resultPageFactory;
        parent::__construct($context);
    }


    /**
     * ProductAttachment view page
     *
     * @return \Magento\Framework\App\ResponseInterface|\Magento\Framework\Controller\ResultInterface|\Magento\Framework\View\Result\Page|void
     */
    public function execute()
    {
        if (!$this->productAttachmentLoader->load($this->_request, $this->_response)) {
            return;
        }

        /** @var \Magento\Framework\View\Result\Page $resultPage */
        $resultPage = $this->resultPageFactory->create();
        return $resultPage;
    }
}
